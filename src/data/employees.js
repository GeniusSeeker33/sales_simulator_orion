export function getEmployeeDisplayName(employee) {
  const preferred = employee.preferredName?.trim();
  if (preferred) return preferred;
  return employee.firstName;
}

export function getEmployeeFullName(employee) {
  return `${employee.firstName} ${employee.lastName}`.trim();
}

export function getEmployeeByCode(code) {
  return employees.find((employee) => employee.code === code) ?? null;
}

export function getEmployeeByEmail(email) {
  return (
    employees.find(
      (employee) => employee.email.toLowerCase() === String(email).toLowerCase()
    ) ?? null
  );
}

export function sortEmployeesByHireDate(list = employees) {
  return [...list].sort(
    (a, b) => new Date(a.hireDate).getTime() - new Date(b.hireDate).getTime()
  );
}